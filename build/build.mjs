// AIOS design-token build.
// Source of truth: ../tokens/*.json (DTCG). Outputs (all generated â€” never hand-edit):
//   dist/tokens.css          :root (light + shared) + .dark (color overrides)
//   dist/tailwind-theme.css  Tailwind v4 @theme inline bridge (shadcn utility names -> runtime vars)
//   dist/tokens.pencil.json  flat name->value maps for the Pencil MCP set_variables call
import StyleDictionary from 'style-dictionary';
import { readFileSync, writeFileSync, rmSync, mkdirSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const tokensDir = join(root, 'tokens');
const dist = join(root, 'dist');
mkdirSync(dist, { recursive: true });

const SHARED = ['type.json', 'space.json', 'radius.json', 'effect.json', 'gradient.json'];

// color.bg -> aios-bg ; font.display -> aios-font-display ; space.1 -> aios-space-1
const varName = (path) => {
  const segs = path[0] === 'color' ? path.slice(1) : path;
  return `aios-${segs.join('-')}`;
};
const valueOf = (t) => (t.$value !== undefined ? t.$value : t.value);

StyleDictionary.registerFormat({
  name: 'aios/css',
  format: ({ dictionary, options }) => {
    const selector = options.selector ?? ':root';
    const lines = dictionary.allTokens.map((t) => `  --${varName(t.path)}: ${valueOf(t)};`);
    return `${selector} {\n${lines.join('\n')}\n}\n`;
  },
});

const run = (sources, selector, destination) =>
  new StyleDictionary({
    usesDtcg: true,
    source: sources,
    platforms: {
      css: {
        transforms: [],
        buildPath: 'dist/',
        files: [{ destination, format: 'aios/css', options: { selector } }],
      },
    },
    log: { verbosity: 'silent', warnings: 'disabled' },
  }).buildAllPlatforms();

// 1) CSS â€” two passes, then concatenate into one file.
await run([...SHARED.map((f) => join(tokensDir, f)), join(tokensDir, 'color.light.json')], ':root', '_root.css');
await run([join(tokensDir, 'color.dark.json')], '.dark', '_dark.css');

const header = `/* AIOS design tokens â€” GENERATED by build/build.mjs. Do not edit by hand. */\n/* Light is the :root default; add class="dark" on <html> to switch. */\n\n`;
const tokensCss =
  header +
  readFileSync(join(dist, '_root.css'), 'utf8') +
  '\n' +
  readFileSync(join(dist, '_dark.css'), 'utf8');
writeFileSync(join(dist, 'tokens.css'), tokensCss);
rmSync(join(dist, '_root.css'));
rmSync(join(dist, '_dark.css'));

// 2) Tailwind v4 bridge. Maps shadcn/Tailwind utility namespaces to our runtime vars so
//    `bg-background`, `text-foreground`, `border-border`, `bg-primary`, etc. switch with .dark.
//    `inline` keeps the var() reference at runtime instead of inlining the light value.
const tw = `/* AIOS Tailwind v4 theme bridge â€” GENERATED. Import AFTER tokens.css. */
@theme inline {
  /* shadcn neutral slots */
  --color-background:            var(--aios-bg);
  --color-foreground:            var(--aios-fg);
  --color-card:                  var(--aios-surface);
  --color-card-foreground:       var(--aios-fg);
  --color-popover:               var(--aios-elevated);
  --color-popover-foreground:    var(--aios-fg);
  --color-secondary:             var(--aios-elevated);
  --color-secondary-foreground:  var(--aios-fg);
  --color-muted:                 var(--aios-muted);
  --color-muted-foreground:      var(--aios-fg-muted);
  /* shadcn "accent" is a subtle hover surface, NOT the brand accent */
  --color-accent:                var(--aios-muted);
  --color-accent-foreground:     var(--aios-fg);
  --color-border:                var(--aios-border);
  --color-border-visible:        var(--aios-border-visible);
  --color-border-strong:         var(--aios-border-strong);
  --color-input:                 var(--aios-border-visible);
  --color-ring:                  var(--aios-ring);

  /* brand + supporting accents */
  --color-primary:               var(--aios-primary);
  --color-primary-foreground:    var(--aios-primary-fg);
  --color-brand:                 var(--aios-primary);
  --color-lime:                  var(--aios-accent);
  --color-lime-foreground:       var(--aios-accent-fg);
  --color-emerald:               var(--aios-emerald);
  --color-amber:                 var(--aios-amber);
  --color-cyan:                  var(--aios-cyan);
  --color-fuchsia:               var(--aios-fuchsia);
  --color-destructive:           var(--aios-destructive);
  --color-destructive-foreground:var(--aios-destructive-fg);

  /* type */
  --font-display: var(--aios-font-display);
  --font-sans:    var(--aios-font-body);
  --font-mono:    var(--aios-font-mono);

  /* radius */
  --radius-sm:  var(--aios-radius-sm);
  --radius-md:  var(--aios-radius-md);
  --radius-lg:  var(--aios-radius-lg);
  --radius-xl:  var(--aios-radius-xl);
  --radius-2xl: var(--aios-radius-2xl);

  /* elevation â€” mode-aware: subtle shadows in light, none/overlay-only in dark */
  --shadow-button:  var(--aios-shadow-button);
  --shadow-card:    var(--aios-shadow-card);
  --shadow-overlay: var(--aios-shadow-overlay);
}
`;
writeFileSync(join(dist, 'tailwind-theme.css'), tw);

// 3) Pencil variable map â€” flat name->value per group, both color modes.
const load = (f) => JSON.parse(readFileSync(join(tokensDir, f), 'utf8'));
const flatten = (obj, prefix = '') => {
  const out = {};
  for (const [k, v] of Object.entries(obj)) {
    if (v && typeof v === 'object' && '$value' in v) out[prefix + k] = v.$value;
    else if (v && typeof v === 'object') Object.assign(out, flatten(v, `${prefix}${k}-`));
  }
  return out;
};
const pencil = {
  $description: 'AIOS design tokens for Pencil set_variables. Generated â€” do not edit.',
  colorsLight: flatten(load('color.light.json').color),
  colorsDark: flatten(load('color.dark.json').color),
  shadowsLight: flatten(load('color.light.json').shadow),
  shadowsDark: flatten(load('color.dark.json').shadow),
  font: flatten(load('type.json').font),
  text: flatten(load('type.json').text),
  weight: flatten(load('type.json').weight),
  leading: flatten(load('type.json').leading),
  tracking: flatten(load('type.json').tracking),
  space: flatten(load('space.json').space),
  radius: flatten(load('radius.json').radius),
  glow: flatten(load('effect.json').glow),
  gradient: flatten(load('gradient.json').gradient),
};
writeFileSync(join(dist, 'tokens.pencil.json'), JSON.stringify(pencil, null, 2) + '\n');

const files = readdirSync(dist).sort();
console.log('Built dist/:', files.join(', '));
